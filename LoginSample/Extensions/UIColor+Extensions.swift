import UIKit

extension UIColor {
    enum Assets {
        static let accent = UIColor(named: "Accent")!
        static let accentActive = UIColor(named: "AccentActive")!
        static let background = UIColor(named: "Background")!
        static let error = UIColor(named: "Error")!
        static let neutral01 = UIColor(named: "Neutral01")!
        static let neutral02 = UIColor(named: "Neutral02")!
        static let neutral03 = UIColor(named: "Neutral03")!
        static let primary01 = UIColor(named: "Primary01")!
        static let primary02 = UIColor(named: "Primary02")!
        static let primary03 = UIColor(named: "Primary03")!
        static let primary04 = UIColor(named: "Primary04")!
        static let text = UIColor(named: "Text")!
    }
}
